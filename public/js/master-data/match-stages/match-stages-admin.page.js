/**
 * @fileoverview Admin match stages management page.
 * @module master-data/match-stages/match-stages-admin.page
 */

import { showLoadingOverlay, hideLoadingOverlay } from '../../components/loading-overlay.component.js';
import { showConfirmationModal } from '../../components/confirmation-modal.component.js';
import { showSuccessToast, showErrorToast } from '../../utils/toast.util.js';
import { AuthorizationService } from '../../authorization/authorization.service.js';
import { Permissions } from '../../authorization/permission.constants.js';
import { MATCH_STAGE_MESSAGES, MATCH_STAGE_ROUTES } from './match-stage.constants.js';
import {
  createMatchStage,
  deleteMatchStage,
  getMatchStageById,
  getMatchStageErrorMessage,
  listMatchStages,
  updateMatchStage,
} from './match-stage.service.js';
import {
  applyFormErrors,
  mountMatchStageListLoading,
  readMatchStageForm,
  renderMatchStageFormPage,
  renderMatchStageListPage,
  renderMatchStageNotFound,
} from './match-stage.renderer.js';
import { Logger } from '../../utils/logger.util.js';

/**
 * @typedef {import('./match-stage.service.js').MatchStage} MatchStage
 */

/**
 * @param {HTMLElement} outlet
 * @returns {void}
 */
export function render(outlet) {
  void initMatchStagesAdminPage(outlet);
}

/**
 * @param {HTMLElement} outlet
 * @returns {Promise<void>}
 */
async function initMatchStagesAdminPage(outlet) {
  await AuthorizationService.resolve();

  if (!AuthorizationService.hasPermission(Permissions.MANAGE_MATCH_STAGES)) {
    outlet.innerHTML = renderMatchStageNotFound(MATCH_STAGE_MESSAGES.PERMISSION_DENIED);
    return;
  }

  const params = new URLSearchParams(window.location.search);
  const action = params.get('action');
  const stageId = params.get('id');

  if (action === 'create') {
    outlet.innerHTML = renderMatchStageFormPage(null, { isCreate: true });
    bindMatchStageForm(outlet, null);
    return;
  }

  if (stageId) {
    await renderEditView(outlet, stageId);
    return;
  }

  await renderListView(outlet);
}

/**
 * @param {HTMLElement} outlet
 * @returns {Promise<void>}
 */
async function renderListView(outlet) {
  mountMatchStageListLoading(outlet);
  showLoadingOverlay(MATCH_STAGE_MESSAGES.LOADING);

  try {
    const stages = await listMatchStages({ forceRefresh: true, includeDefaults: false });
    outlet.innerHTML = renderMatchStageListPage(stages);
  } catch (error) {
    Logger.error('[MatchStagesAdmin] List failed:', error);
    outlet.innerHTML = renderMatchStageNotFound(getMatchStageErrorMessage(error));
    showErrorToast(getMatchStageErrorMessage(error));
  } finally {
    hideLoadingOverlay();
  }
}

/**
 * @param {HTMLElement} outlet
 * @param {string} stageId
 * @returns {Promise<void>}
 */
async function renderEditView(outlet, stageId) {
  mountMatchStageListLoading(outlet);
  showLoadingOverlay(MATCH_STAGE_MESSAGES.LOADING_STAGE);

  try {
    const stage = await getMatchStageById(stageId);

    if (!stage) {
      outlet.innerHTML = renderMatchStageNotFound();
      return;
    }

    outlet.innerHTML = renderMatchStageFormPage(stage, { isCreate: false });
    bindMatchStageForm(outlet, stage);
  } catch (error) {
    Logger.error('[MatchStagesAdmin] Load failed:', error);
    outlet.innerHTML = renderMatchStageNotFound(getMatchStageErrorMessage(error));
    showErrorToast(getMatchStageErrorMessage(error));
  } finally {
    hideLoadingOverlay();
  }
}

/**
 * @param {HTMLElement} outlet
 * @param {MatchStage|null} stage
 * @returns {void}
 */
function bindMatchStageForm(outlet, stage) {
  const form = outlet.querySelector('#ptw-stage-form');

  if (!(form instanceof HTMLFormElement)) {
    return;
  }

  bindAutoGeneratedValue(form, stage);

  form.addEventListener('submit', async (event) => {
    event.preventDefault();
    const payload = readMatchStageForm(form);

    showLoadingOverlay(stage ? MATCH_STAGE_MESSAGES.UPDATING : MATCH_STAGE_MESSAGES.CREATING);

    try {
      if (stage) {
        await updateMatchStage(stage.id, payload);
        showSuccessToast(MATCH_STAGE_MESSAGES.UPDATED);
        await renderEditView(outlet, stage.id);
      } else {
        await createMatchStage(payload);
        showSuccessToast(MATCH_STAGE_MESSAGES.CREATED);
        window.history.pushState({}, '', MATCH_STAGE_ROUTES.ADMIN_LIST);
        await renderListView(outlet);
      }
    } catch (error) {
      if (typeof error === 'object' && error !== null && 'validation' in error) {
        applyFormErrors(form, /** @type {{ validation: { errors: Record<string, string> } }} */ (error).validation.errors);
      }

      showErrorToast(getMatchStageErrorMessage(error));
    } finally {
      hideLoadingOverlay();
    }
  });

  const deleteButton = outlet.querySelector('[data-ptw-stage-delete]');

  if (deleteButton instanceof HTMLButtonElement && stage) {
    deleteButton.addEventListener('click', async () => {
      const confirmed = await showConfirmationModal({
        title: 'Delete Match Stage',
        message: MATCH_STAGE_MESSAGES.CONFIRM_DELETE,
        confirmLabel: 'Delete',
        confirmClass: 'btn-danger',
      });

      if (!confirmed) {
        return;
      }

      showLoadingOverlay(MATCH_STAGE_MESSAGES.DELETING);

      try {
        await deleteMatchStage(stage.id);
        showSuccessToast(MATCH_STAGE_MESSAGES.DELETED);
        window.history.pushState({}, '', MATCH_STAGE_ROUTES.ADMIN_LIST);
        await renderListView(outlet);
      } catch (error) {
        showErrorToast(getMatchStageErrorMessage(error));
      } finally {
        hideLoadingOverlay();
      }
    });
  }
}

/**
 * @param {HTMLFormElement} form
 * @param {MatchStage|null} stage
 * @returns {void}
 */
function bindAutoGeneratedValue(form, stage) {
  if (stage) {
    return;
  }

  const labelInput = form.elements.namedItem('label');
  const valueInput = form.elements.namedItem('value');

  if (!(labelInput instanceof HTMLInputElement) || !(valueInput instanceof HTMLInputElement)) {
    return;
  }

  labelInput.addEventListener('input', () => {
    valueInput.value = labelInput.value
      .toLowerCase()
      .replace(/[^a-z0-9\s_]/g, '')
      .replace(/\s+/g, '_')
      .replace(/_+/g, '_')
      .replace(/^_+|_+$/g, '');
  });
}

